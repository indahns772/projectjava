    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        String nosim=this.txtnosim.getText();

        try {
            Connection c=ClassDatabase.getkoneksi();
            String sql = "Delete from transaksi Where nosim=?";
            PreparedStatement p=(PreparedStatement) c.prepareStatement(sql);
            p.setString(1, nosim);

            p.executeUpdate();
            p.close();
            JOptionPane.showMessageDialog(this, "Sukses Hapus Data");
        }catch(SQLException e){
            System.out.println(e);
        }finally{
        }
        try {

            Connection c=ClassDatabase.getkoneksi();
            Statement s= c.createStatement();
            String sql="select * from transaksi";
            ResultSet r=s.executeQuery(sql);
            tbl_transaksi.setModel(DbUtils.resultSetToTableModel(r));
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_hapusActionPerformed

