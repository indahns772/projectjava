    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
       String nosim=this.txtnosim.getText();
       String platnomor=this.txtplatnomor.getText();
       java.util.Date tgl_mnjm=(java.util.Date)this.txttglmnjm.getDate();
       java.util.Date tgl_kmbl=(java.util.Date)this.txttglkmbl.getDate();
       String lama=this.txthari.getText();
       String total=this.txttotal.getText();

        if(txtnosim.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Data Belum Lengkap, Id Mobil Tidak Boleh Kosong");
        }
                else{
                    if (txtplatnomor.getText().isEmpty())
                    {
                        JOptionPane.showMessageDialog(this, "Data Belum Lengkap, Harga Rental Tidak Boleh Kosong");
                    }
                    else{
                    if (txthari.getText().isEmpty())
                    {
                        JOptionPane.showMessageDialog(this, "Data Belum Lengkap, Harga Rental Tidak Boleh Kosong");
                    }   
                        else{
                            try {
                                Connection c=ClassDatabase.getkoneksi();
                                String sql = "Insert into transaksi values (?,?,?,?,?,?)";
                                PreparedStatement p=(PreparedStatement) c.prepareStatement(sql);
                                p.setString(1, nosim);
                                p.setString(2, platnomor);
                                p.setDate(3, new java.sql.Date(tgl_mnjm.getTime()));
                                p.setDate(4, new java.sql.Date(tgl_kmbl.getTime()));
                                p.setString(5, lama);
                                p.setString(6, total);
                                p.executeUpdate();
                                p.close();

                                JOptionPane.showMessageDialog(this, "Sukses Tambah Data");

                            }catch(SQLException e){
                                System.out.println(e);
                            }finally{
                            }}
                            try {

                                Connection c=ClassDatabase.getkoneksi();
                                Statement s= c.createStatement();
                                String sql="select * from transaksi";
                                ResultSet r=s.executeQuery(sql);
                                tbl_transaksi.setModel(DbUtils.resultSetToTableModel(r));
                            }catch (Exception e){
                                JOptionPane.showMessageDialog(null, e);
                            }
                        }}
    }//GEN-LAST:event_simpanActionPerformed
    
